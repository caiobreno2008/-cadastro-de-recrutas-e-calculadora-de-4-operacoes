// Defina valores fixos para os números
let number1 = 10; // Exemplo de primeiro número
let number2 = 5;  // Exemplo de segundo número

// Realize as operações matemáticas
let soma = number1 + number2;
let subtracao = number1 - number2;
let multiplicacao = number1 * number2;
let divisao = number1 / number2;

// Mostre os resultados no console
console.log("Resultados:");
console.log(`${number1} + ${number2} = ${soma}`);
console.log(`${number1} - ${number2} = ${subtracao}`);
console.log(`${number1} * ${number2} = ${multiplicacao}`);
console.log(`${number1} / ${number2} = ${divisao.toFixed(2)}`); // Usa toFixed(2) para formatar a divisão com duas casas decimais
