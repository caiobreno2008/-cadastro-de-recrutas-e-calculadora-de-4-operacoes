let number1 = parseInt(prompt("Digite um número."))
let number2 = parseInt(prompt("Digite outro número."))


let soma = number1 + number2
let subtracao = number1 - number2
let multiplicacao = number1 * number2
let divisao = number1 / number2


alert(`Resultados:`);
alert(`${number1} + ${number2} = ${soma}`)
alert(`${number1} - ${number2} = ${subtracao}`)
alert(`${number1} * ${number2} = ${multiplicacao}`)
alert(`${number1} / ${number2} = ${divisao}`)